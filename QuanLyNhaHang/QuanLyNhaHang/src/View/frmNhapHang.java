    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        Double thanhtien =Double.parseDouble(txtSL.getText())*Double.parseDouble(lbDonGia.getText());
        BigDecimal tt= new BigDecimal(thanhtien);
        dal_NhapHang.updateCTHDN(Integer.parseInt(txtSL.getText()), tt, lbIDCTHDN.getText() );
        loadCTHDN(lbIDHDN.getText());
        loadData();
    }//GEN-LAST:event_btnSuaActionPerformed

