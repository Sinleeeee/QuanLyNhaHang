    void loadData()
    {
        dtm.setRowCount(0);
        String []tieude ={"Mã HD","Ngày tạo","Ghi chú", "Tổng"};
        dtm.setColumnIdentifiers(tieude);
        TableColumnModel columnModel = table.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(200);
        columnModel.getColumn(1).setPreferredWidth(50);
        columnModel.getColumn(2).setPreferredWidth(5);
        columnModel.getColumn(3).setPreferredWidth(5);
        //Ẩn đi

        
        tbHD.setModel(dtm);
         List<Object[]> list = dal_NhapHang.loadthongtinHDN();
        for (Object[] row : list) {
             dtm.addRow(row);
            }
        
        
        
          String []tieude1 ={"ID","Tên nguyên liệu","Nhà cung cấp","Đơn vị","SL","Đơn giá","Tổng"};
        dtm1.setColumnIdentifiers(tieude1);
         TableColumnModel columnModel1 = table1.getColumnModel();
        columnModel1.getColumn(0).setPreferredWidth(50);
        columnModel1.getColumn(1).setPreferredWidth(100);
        columnModel1.getColumn(2).setPreferredWidth(100);
         columnModel1.getColumn(3).setPreferredWidth(5);
        columnModel1.getColumn(4).setPreferredWidth(5);
         columnModel1.getColumn(5).setPreferredWidth(5);
        columnModel1.getColumn(6).setPreferredWidth(5);
         tbCTHDN.setModel(dtm1);
        
    }
    
    void loadCTHDN(String idHDN)
    {dtm1.setRowCount(0);
          List<Object[]> list = dal_NhapHang.loadthongtinCTHDN(idHDN);
        for (Object[] row : list) {
             dtm1.addRow(row);
            }
    }

